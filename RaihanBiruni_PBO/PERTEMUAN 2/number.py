# Mendefinisikan beberapa variabel number
angka_pertama = float (input("Masukan angka Pertama"))
angka_kedua = float (input("Masukan angka ke dua"))
hasil_penjumlahan = angka_pertama + angka_kedua
hasil_pengurangan = angka_pertama - angka_kedua
hasil_perkalian = angka_pertama * angka_kedua
hasil_pembagian = angka_pertama / angka_kedua

# Menampilkan hasil perhitungan
print(f"Angka Pertama: {angka_pertama}")
print(f"Angka Kedua: {angka_kedua}")
print(f"Hasil Penjumlahan: {hasil_penjumlahan}")
print(f"Hasil Pengurangan: {hasil_pengurangan}")
print(f"Hasil Perkalian: {hasil_perkalian}")
print(f"Hasil Pembagian: {hasil_pembagian}")
